export function somar2numeros(a, b) {
    if( typeof a !== "number" || typeof b !== "number") {
        return undefined
    }
    return a + b;
}

export function subtrair2numeros(a, b) {
    if( typeof a !== "number" || typeof b !== "number") {
        return undefined
    }
    return a - b;
}

export function multiplicar2numeros(a, b) {
    if( typeof a !== "number" || typeof b !== "number") {
        return undefined
    }
    return a * b;
}